package com.itbank.TechFarm.tfNoteDTO;

public class NoteDTO {
	private int num;	
	private String title;
	private String content;
	private String id;
	private int cbyte;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getCbyte() {
		return cbyte;
	}
	public void setCbyte(int cbyte) {
		this.cbyte = cbyte;
	}
	

	
}
